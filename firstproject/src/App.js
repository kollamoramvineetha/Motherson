class App extends Component {
  state = {
    activeTabId: imagesList[0].id,
  }

  clickTabItem = id => {
    this.setState({activeTabId: id})
  }

  render() {
    const {activeTabId} = this.state
    const {imageUrl, imageAltText} = imagesList[activeTabId]

    return (
      <div className="card">
        <div>
          <img src={imageUrl} alt={imageAltText} className="card1" />
          <h1>Nature Photography</h1>
          <p>Nature Photography by Rahul</p>
          <ul>
            {imagesList.map(each => (
              <Gallery
                key={each.id}
                details={each}
                clickTabItem={this.clickTabItem}
                isActive={activeTabId === each.id}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

export default App;
